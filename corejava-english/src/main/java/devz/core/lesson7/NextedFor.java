package devz.core.lesson7;

public class NextedFor {

    public static void main(String[] args) {

        int age = 10;
        for (int i = 0; i < age; i++) {
            
            for (int j = 0; j < 3; j++) {
                System.out.print("Ravi");
            }
            
            System.out.println("");
        }

    }

}
